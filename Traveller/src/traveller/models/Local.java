package traveller.models;

public class Local {

	private String localOpcao;
	private String localUsuario;
	
	
	public String getLocalOpcao() {
		return localOpcao;
	}
	public void setLocalOpcao(String localOpcao) {
		this.localOpcao = localOpcao;
	}
	public String getLocalUsuario() {
		return localUsuario;
	}
	public void setLocalUsuario(String localUsuario) {
		this.localUsuario = localUsuario;
	}
	
	
}
